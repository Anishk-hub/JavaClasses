public class PositiveOrNegative {
    public static void main(String[] args) {
        int num = -10;
        if(num > 0){
            System.out.println("Number is positive:"+num);
        } else if (num < 0) {
            System.out.println("Number is negative:"+num);

        } else
        {
            System.out.println(" Number is Zero");
        }

    }
}
